    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:
        UsuariosDAO usuariosDAO = new UsuariosDAO();
        List lista;
        if ((jTxtTotal.getText().isEmpty() == false) && (jTxtTotal.getText().isEmpty() == false)) {
            lista = (List) usuariosDAO.listNomeNivel(jTxtTotal.getText(), jCboDhmClientes.getSelectedIndex());
        } else if (jTxtTotal.getText().isEmpty() == false) {
            lista = (List) usuariosDAO.listNome(jTxtTotal.getText());
        } else if (jCboDhmClientes.getSelectedIndex() != -1) {
            lista = (List) usuariosDAO.listNivel(jCboDhmClientes.getSelectedIndex());
        } else {
            lista = (List) usuariosDAO.listAll();
        }
        controllerConsultasUsuarios.setList(lista);
    }//GEN-LAST:event_jBtnConsultarActionPerformed

